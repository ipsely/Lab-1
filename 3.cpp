#include <iostream>
using namespace std;

int main() {
	  int a, b, sum, subtract, multiply, divide;
  cout << "Enters two numbers : ";
  cin >> a >> b;
  
  sum = a + b;
  cout << "The sum is " << sum;
  
  subtract = a - b;
  cout << "The subtraction is " << subtract;
  
  multiply = a * b;
  cout << "The multiplication is " << multiply;
  
  divide = a / b;
  cout << "The division is " << divide;
 
}
